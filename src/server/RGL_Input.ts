export class RGL_Input {
    readonly cursor: {
        x: number;
        y: number;
        isDown: boolean;
    } = { x: 0, y: 0, isDown: false };
    readonly keys: boolean[] = new Array(255).fill(false);

    isKeyDown(key: Keys) {
        return this.keys[key];
    }
}
